void adc_init()
{
  //Turn on bit 6. All other bits need to be off.
  ADMUX = 0x40;
  
  //Set up ADCSRA. Use a prescaler division factor of 16
  ADCSRA = (1<<ADEN)|(1<<ADSC)|(1<<ADATE)|(0<<ADIE)|(1<<ADPS2)|(0<<ADPS1)|
  (0<<ADPS0);
}

//==============================================================================
uint16_t adc_get_value()
{
  return ADC;
}

