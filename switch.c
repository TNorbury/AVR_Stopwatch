void switch_init()
{
  
  //Set the switch to input.
  DDRB &= ~(1<<4);   
}


//==============================================================================
uint8_t switch_get_position()
{
  
  //Turn off all bits expect for the 4th bit, and then shift that bit 4 places
  // to the right. Return that value, which will either be 0 or 1.
  return ((PINB & 0x10) >> 4);
}

